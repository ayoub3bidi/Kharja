package Beans;

public class RegionBean {
	private int idRegion;
	private String Nom_Region;
	private String Description_Region;
	private String imgRegion;
	
	public int getIdRegion() {
		return idRegion;
	}
	public void setIdRegion(int idRegion) {
		this.idRegion = idRegion;
	}
	public String getNom_Region() {
		return Nom_Region;
	}
	public void setNom_Region(String nom_Region) {
		Nom_Region = nom_Region;
	}
	public String getDescription_Region() {
		return Description_Region;
	}
	public void setDescription_Region(String description_Region) {
		Description_Region = description_Region;
	}
	public String getImgRegion() {
		return imgRegion;
	}
	public void setImgRegion(String imgRegion) {
		this.imgRegion = imgRegion;
	}
	
	
	
	
}
